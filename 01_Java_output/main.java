
public class Main {

    public static void main(String[] args) {
        System.out.println("Hello World!"); //prints text in a new line

        // we can add more than one println() statements
        System.out.println("hey");
        System.out.println("heyo");

        System.out.print("Hello World!"); //prints text in the same line
        System.out.print("hey");
        System.out.print("heyo"
        );

        //escape sequeance
        System.out.println("Hello \"world\"!");

        //print numbers
        System.out.println(1);
        System.out.println(2);

        // we can  also perform operations on numbers in println() or print() methods
        System.out.println(1 + 2);
    }
}
